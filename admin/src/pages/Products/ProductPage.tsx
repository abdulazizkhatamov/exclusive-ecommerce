import { useQuery } from "react-query";
import { getProduct } from "@/api/api-products.ts";

import { Button } from "@/components/ui/button.tsx";
import {
  Tabs,
  TabsContent,
  TabsList,
  TabsTrigger,
} from "@/components/ui/tabs.tsx";
